    private void cargarDatos() {
    DefaultTableModel modeloIda = (DefaultTableModel) tablaVuelosIda.getModel();
    DefaultTableModel modeloRegreso = (DefaultTableModel) tablaVuelosRegreso.getModel();

    modeloIda.setRowCount(0);
    modeloRegreso.setRowCount(0);

    // 游댳 IDA: A침adimos fila[3] que es el precio
    for (Object[] fila : util.DatosReserva.vuelosIda) {
        modeloIda.addRow(new Object[]{
            fila[0], // Numero Persona
            fila[1], // Numero Asiento
            fila[2], // Hora de Vuelo
            fila[3]  // Precio <--- ESTA ES LA QUE FALTA
        });
    }

    // 游댳 REGRESO: A침adimos fila[3] que es el precio
    for (Object[] fila : util.DatosReserva.vuelosRegreso) {
        modeloRegreso.addRow(new Object[]{
            fila[0], 
            fila[1], 
            fila[2], 
            fila[3]  // Precio <--- ESTA ES LA QUE FALTA
        });
    }

    // 游댳 TOTAL: Aplicamos el descuento si existe
    double subtotal = util.DatosReserva.total;
    double descuento = subtotal * vista.Form_MenuPrincipal.descuentoAplicado;
    double totalFinal = subtotal - descuento;
    
    lblDineroPagar.setText(String.valueOf(totalFinal));
}
    
