    private void btnPagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPagarActionPerformed
    // 1. Obtener los textos y limpiar espacios
    String nombre = txtNombre.getText().trim();
    String documento = txtDocumento.getText().trim();
    String correo = txtCorreo.getText().trim().toLowerCase();
    String celular = txtCelular.getText().trim();
    String tarjeta = txtNumeroTarjeta.getText().trim().replace(" ", ""); // Quitamos espacios si los hay
    String cvi = txtCVI.getText().trim();

    // VALIDACIONES

    // Nombre: Solo letras y espacios
    if (!nombre.matches("^[a-zA-Z\\s]+$")) {
        mostrarError("El nombre solo debe contener letras.");
        return;
    }

    // Documento ID: 10 n칰meros O formato TA-00000
    if (!documento.matches("^(\\d{10}|VTX-\\d{5})$")) {
        mostrarError("Documento inv치lido. Use 10 d칤gitos (1757384960) o formato TA-XXXXX (VTX-38583).");
        return;
    }

    // Correo: Formato est치ndar @ejemplo.com
    if (!correo.matches("^[a-z0-0._%+-]+@[a-z0-9.-]+\\.[a-z]{2,6}$")) {
        mostrarError("Correo electr칩nico no v치lido (ejemplo@correo.com).");
        return;
    }
    txtCorreo.setText(correo); // Actualizamos el campo a min칰sculas, el correo siempre suele estar en minusculas

    // Tel칠fono: Estricto 099 435 2432 (permitiendo espacios o sin ellos)
    if (celular.startsWith("+")) {
        mostrarError("No use prefijos internacionales (+). Reemplace por '0'.");
        return;
    }
    if (!celular.matches("^0\\d{9}$") && !celular.matches("^0\\d{2}\\s\\d{3}\\s\\d{4}$")) {
        mostrarError("Formato de celular requerido: 099 435 2432");
        return;
    }

    // Tarjeta: M칤nimo 16 n칰meros
    if (!tarjeta.matches("^\\d{16,}$")) {
        mostrarError("La tarjeta debe tener al menos 16 n칰meros.");
        return;
    }

    // CVI: Exactamente 3 n칰meros
    if (!cvi.matches("^\\d{3}$")) {
        mostrarError("El CVI debe ser de exactamente 3 n칰meros.");
        return;
    }

    // FIN DE VALIDACIONES

    // 1. Detectar el costo real desde la interfaz
    double subtotal = 0;
    try {
        // Intentamos leer el precio desde el campo de texto de precio o el label
        subtotal = Double.parseDouble(lblDineroPagar.getText()); 
    } catch (NumberFormatException e) {
        // Si falla, intentamos con la variable global por si acaso
        subtotal = util.DatosReserva.total;
    }

    // 2. Calcular con el descuento guardado en el Men칰 Principal
    double descuentoPorcentaje = Form_MenuPrincipal.descuentoAplicado;
    double montoDescuento = subtotal * descuentoPorcentaje;
    double totalFinal = subtotal - montoDescuento;

    // 3. Generar el texto de la Factura
    String facturaTexto = "==========VTX-AIRLINES==========\n"
            + "CLIENTE: " + txtNombre.getText().toUpperCase() + "\n"
            + "ID: " + txtDocumento.getText() + "\n"
            + "----------------------------------\n"
            + "SUBTOTAL: $" + subtotal + " USD\n"
            + "DESCUENTO (" + (descuentoPorcentaje * 100) + "%): -$" + montoDescuento + " USD\n"
            + "TOTAL PAGADO: $" + totalFinal + " USD\n"
            + "----------------------------------\n"
            + "춰Gracias por elegir VTX-Airlines!";

    // 4. Mostrar y preguntar por impresi칩n
    JOptionPane.showMessageDialog(this, facturaTexto, "Pago Exitoso", JOptionPane.INFORMATION_MESSAGE);

    int respuesta = JOptionPane.showConfirmDialog(this, "쮻esea imprimir su comprobante?", "Imprimir", JOptionPane.YES_NO_OPTION);
    if (respuesta == JOptionPane.YES_OPTION) {
        imprimirFactura(facturaTexto);
    }

    new Form_Principal().setVisible(true);
    this.dispose();
}

