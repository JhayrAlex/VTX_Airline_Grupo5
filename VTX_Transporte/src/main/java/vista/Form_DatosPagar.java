    private void imprimirFactura(String contenido) {
    try {
        javax.swing.JTextArea areaImpresion = new javax.swing.JTextArea();
        areaImpresion.setText(contenido);
        boolean completado = areaImpresion.print(); // Abre el di치logo de impresi칩n
        if (completado) {
            JOptionPane.showMessageDialog(this, "Impresi칩n finalizada");
        }
    } catch (java.awt.print.PrinterException e) {
        JOptionPane.showMessageDialog(this, "Error de impresi칩n: " + e.getMessage());
    }
}
